#include <iostream>

int main() {
    std::cout << "Size of integer variable type is: " << sizeof(int) << " bit/s" << std::endl;
    std::cout << "Size of double variable type is: " << sizeof(double) << " bit/s" << std::endl;
    std::cout << "Size of character variable type is: " << sizeof(char) << " bit/s" << std::endl;
    std::cout << "Size of boolean variable type is: " << sizeof(bool) << " bit/s" << std::endl;
    
    return 0;
}